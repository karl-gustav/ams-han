package main

import (
	"encoding/json"
	"fmt"

	ams "github.com/karl-gustav/ams-han"
)

func main() {
	c := testChannel()
	next := ams.ByteReader(c)
	for {
		bytePackage, err := next()
		if err != nil {
			fmt.Println(err)
			if err == ams.CHANNEL_IS_CLOSED_ERROR {
				return
			}
			continue
		}
		message, err := ams.BytesParser(bytePackage)
		if err != nil {
			fmt.Println(err)
			continue
		}
		j, _ := json.Marshal(message)
		fmt.Printf("%s\n", j)
	}
}

